package com.pcs.ztqtj.view.myview.chart;

import java.util.ArrayList;
import java.util.List;

/**
 * 图表数据系列
 * @author E.Sun
 * 2015年12月4日
 */
public class XYMultipleSeries {

	private List<XYSeries> seriesList = new ArrayList<XYSeries>();

	private List<String> xLabels = new ArrayList<String>();
	
	public void addXYSeries(XYSeries series) {
		seriesList.add(series);
	}
	
	public List<XYSeries> getSeriesList() {
		return seriesList;
	}
	
	public void addXLabel(String label) {
		xLabels.add(label);
	}
	
	public void addXLabels(List<String> labels) {
		xLabels.addAll(labels);
	}
	
	public List<String> getXLabels() {
		return xLabels;
	}
	
}
